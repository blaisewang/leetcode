# Definition for a binary tree node.
from typing import List


class TreeNode:
    def __init__(self, val=0, left=None, right=None):
        self.val = val
        self.left = left
        self.right = right


class Solution:
    def preorderTraversal(self, root: TreeNode) -> List[int]:
        r = list()

        def traverse(n: TreeNode):
            if not n:
                return
            r.append(n.val)
            traverse(n.left)
            traverse(n.right)

        traverse(root)
        return r

